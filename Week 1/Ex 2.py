#Addition (+)
Add = 4 + 4
print(Add)  # Output: 8

#Subtraction (-)
Sub = 10 - 8
print(Sub)  # Output: 2

#Multiplication (*)
Mul = 2 * 5
print(Mul)  # Output: 10

#Division (/)
Div = 8 / 4
print(Div)  # Output: 2

#Modulus (%)
Mod = 10 % 3
print(Mod)  # Output: 1 (remainder of 10 divided by 3)

#Exponentiation (**):
Ex = 3 ** 3
print(Ex)  # Output: 8 (3 raised to the power of 3)

#Floor Division (//):
Flo = 10 // 5
print(Flo)  # Output: 2 (integer division of 10 by 2)